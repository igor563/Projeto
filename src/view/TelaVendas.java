    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        if(txt_produto.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this," Digite o nome do produto ");
            Validador.ValidarNumeros(txt_qtd);
            return;
        }else{

            DefaultTableModel dtmProdutos = (DefaultTableModel) tabela.getModel();
            Object[] Dados = {txt_nome.getText(),txt_produto.getText(), txt_qtd.getText()};
            dtmProdutos.addRow(Dados);
        }

    }//GEN-LAST:event_jButton6ActionPerformed

